const int d3_ans[2][3] = {{2, 4, 2}, {3, 3, 11}};

int house_delivery(const char *path, const int nrActors) {
  assert(nrActors < 32);
  int actorId = 0;
  std::vector<std::pair<int, int>> actorLocations(nrActors);
  std::map<std::pair<int, int>, int> visit; // location = visited.
  visit[std::make_pair(0, 0)] = 1;

  while (*path) {
    int &x = actorLocations[actorId].first;
    int &y = actorLocations[actorId].second;

    if (*path == '^') {
      ++y;
    } else if (*path == '>') {
      ++x;
    } else if (*path == 'v') {
      --y;
    } else if (*path == '<') {
      --x;
    } else {
      std::cout << "Failure reading path instruction: " << *path << std::endl;
    }
    // visit[std::make_pair(x, y)] |= 1 << actorId;
    visit[std::make_pair(x, y)] = 1;
    ++path;
    actorId = (actorId + 1) % (nrActors);
  }
  // STUDY: It doesn't matter who visists a location, it is all about actually
  // visiting.
  //   int result = 0;
  //   for (auto kvp : visit) {
  //     result += (kvp.second & 0x10) + (kvp.second & 0x1);
  //   }
  return (int)(visit.size());
}

void d3() {
  CHECK_VALUE(house_delivery(d3_paths[0], 1), d3_ans[0][0]);
  CHECK_VALUE(house_delivery(d3_paths[1], 1), d3_ans[0][1]);
  CHECK_VALUE(house_delivery(d3_paths[2], 1), d3_ans[0][2]);
  CHECK_VALUE(house_delivery(d3_paths[4], 2), d3_ans[1][0]);
  CHECK_VALUE(house_delivery(d3_paths[1], 2), d3_ans[1][1]);
  CHECK_VALUE(house_delivery(d3_paths[2], 2), d3_ans[1][2]);
  std::cout << "\t1: " << house_delivery(d3_paths[3], 1) << std::endl;
  std::cout << "\t2: " << house_delivery(d3_paths[3], 2) << std::endl;
}